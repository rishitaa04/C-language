// increment operator
// ++i and i++
// i++ means use and then increase
// ++i means increase and then use
// same goes by decrement
// --i mtlb pelle decrease and then use whereas
// i++ means use and then decrease

#include <stdio.h>
int main()
{
    int i = 1;
    printf("%d\n", i);
    printf("%d\n", i++);
    printf("%d\n", i);
    printf("%d\n", ++i);
    printf("%d\n", i);
    printf("%d\n", i++);
    printf("%d\n", i);
    printf("%d\n", ++i);
    printf("%d\n", i);

    return 0;
}